class Calculator:
    """Manages calculator functionality."""
    def add(self, a, b):
        """
        Perform operation with a and b.
        
        Args:
            self: The class or instance.
            a: The a.
            b: The b.
        """
        return a + b
    
    def subtract(self, a, b):
        """
        Perform operation with a and b.
        
        Args:
            self: The class or instance.
            a: The a.
            b: The b.
        """
        return a - b
    
    def multiply(self, a, b):
        """
        Perform operation with a and b.
        
        Args:
            self: The class or instance.
            a: The a.
            b: The b.
        """
        return a * b

def divide(x, y):
    """
    Perform operation with x and y.
    
    Args:
        x: The x.
        y: The y.
    """
    return x / y
